def km_to_miles(km):
    ''' (number) -> float
    Return the number of miles equivalent to km kilometers. 
    >>> km_to_miles(16)
    10.0
    '''
    return 1000.0

def miles_to_km(m):
    ''' (number) -> float
    Return the number of kilometers equivalent to m miles.
    >>> miles_to_km(10)
    16.0
    '''
    return (m * 1.6)
